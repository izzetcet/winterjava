package day02scanner;

public class IncrementDecrement01 {

	public static void main(String[] args) {
		
		
		int num1 = 5;  //num1 value is 5
		//increase the value of num1 by 3
		
		num1 = num1 + 3;
		//new num1 value is 8
		
		
		// second way

		num1 += 3;
		
		System.out.println(num1);
		
		
		
		//3.way
		
		num1++; // mean num1+=1 or num1 = num1 +1
		
		
		 //NOT: increment can be done by multiplication as weell
		
		num1 = num1 * 2;
				
		num1*=2;
		System.out.println(num1);
		
		
		
	}

}
