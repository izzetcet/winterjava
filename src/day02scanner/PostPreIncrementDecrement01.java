package day02scanner;

public class PostPreIncrementDecrement01 {

	public static void main(String[] args) {
		
		int x = 12;
		System.out.println(x++); //12 yazior  sonra 1 ekliyor
		System.out.println(x); //13 yaziyor
		
		
		
		System.out.println(++x); // once 1 ekleyip sonra 14yazdirir
		
		
		System.out.println(x--); // 14 yazdirir sonra xe 13 degerini atar
	
	
	System.out.println(--x); //  12 yazdirir
	
	
	System.out.println(x++); //12
	System.out.println(x= x +1); //14
	
	}

}
